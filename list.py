# List: A built in data type that stores set of values.
# It can store elements of different types(int,float,string,etc)


marks = [94.4, 87.5, 95.6, 66.4, 45.1]
print(marks)       # This will print the list
print(len(marks))  # This will write the length of list
print(marks[0])    # This will print the marks at 0th index
print(marks[1])    # This will print the marks at 0th index




