
list = [2, 4, 5, 7, 3]

# Append: add value in the last i.e mutating the list
list.append(1)
print(list)

# Sorting: sort in ascending 
print(list.sort()) #This will just sort the list not print it hence o/p will be none
print(list)        # This will print by default in ascending order

# Sorting descending: sorts in descending order
list.sort(reverse=True) #This will just sort the list 
print(list)  # This will print by default in descending order

# Reverse
list.reverse()
print(list)










