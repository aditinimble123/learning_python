#arithmetic operators (+,-,*,/,%,**)
a = 12
b = 2
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b) #modulus
print(a ** b)  #a^b or power opr


#Relational operators(==,!=,<=,<,>=,>)
c = 50
d = 20
print(c==d)#False
print(c!=d)#True
print(c<=d)#False
print(c<d)#False
print(c>=d)#True
print(c>d)#True


#Assignment operators (=,+=,-=,*=,/=,**=)
num1 = 10
num1 += 10 # add 10 to  existing value
print(num1)
num2 = 10
num2 -= 10 # sub 10 from existing value
print(num2)
num3 = 10
num3 *= 10 # mul 10 with existing value
print(num3)
num4 = 10
num4 /= 2 # div by 2 frm existing value
print(num4)
num5 = 10
num5 %= 2 # gives remainder
print(num5)
num6 = 10
num6 **= 2 #gives raise to
print(num6)

# Logical Operator (not,and,or)
e = 50
f = 20
print("not operator:",not (e > f))

val1 = True
val2 = False
print("and operator:", val1 and val2)
print("or operator:", val1 or val2)

print("and operator:", (e==f) and (e>f)) #can also work on direct statements
print("or operator:", (e==f) or (e>f))





