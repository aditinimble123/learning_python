# ---- Dictionary Example ----
student_marks = {
    "Aditi": 90,
    "Manali": 85,
    "Kimaya": 92
}

print("Student Marks Dictionary:", student_marks)

# Add a new entry
student_marks["Riya"] = 88
print("After adding Riya:", student_marks)

# Access a value
print("Marks of Aditi:", student_marks["Aditi"])

# Change a value
student_marks["Manali"] = 89
print("After updating Manali's marks:", student_marks)